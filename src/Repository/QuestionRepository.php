    public function countFamousQuestion(): array
    {
        return $this->createQueryBuilder('question')
            ->innerJoin('question.reponses', 'reponses')
            ->groupBy('question.id')
            ->having('COUNT(reponses.id) >= 6')
            ->getQuery()
            ->getResult();
        
